# Arithmetic Operators 
a = 2 
b = 3.5 
add = a + b 
minus = b - a 
multiply = a*b  
modulo = b%a 
division = b/a 
power  = b**a 
print(add , minus , multiply ,division , modulo , power)  

# Assignment Operators 
x = 4 
y = 2
y += x # The value of y will be 6
y -= x  # The value of y will be 4
y *= x  # The value of y will be 8
y /= x  # The value of y will be 4. We have // for integer division
y **= x  # The value of y will be 16
y %= x  # The value of y will be 0

# Comparison Operators 
m = 4 
n = 2
m < n # false  
m > n # true 
m <= n # false
m >= n # true 
m != n # true 
m == n #false
# Logical Operators 
z = m > n or m < n #true 
z =  m > n and  m < n  #false 
z =  not(m > n or m < n) #false 
